package com.boe.idm.project.model;

public class ScrapEntity {
	private String factoryDate;
	
	private String dateType;
	
	private String productCategory;
	
	private String factoryName;
	
	private String productionType;
	
	private String causeDepartment;
	
	private String scrapQty;
	
	private String outQty;
	
	private String totalScrap;
	
	private String inQty;
	
	private String causeRatio;

	public String getFactoryDate() {
		return factoryDate;
	}

	public void setFactoryDate(String factoryDate) {
		this.factoryDate = factoryDate;
	}

	public String getDateType() {
		return dateType;
	}

	public void setDateType(String dateType) {
		this.dateType = dateType;
	}

	public String getProductCategory() {
		return productCategory;
	}

	public void setProductCategory(String productCategory) {
		this.productCategory = productCategory;
	}

	public String getFactoryName() {
		return factoryName;
	}

	public void setFactoryName(String factoryName) {
		this.factoryName = factoryName;
	}

	public String getProductionType() {
		return productionType;
	}

	public void setProductionType(String productionType) {
		this.productionType = productionType;
	}

	public String getCauseDepartment() {
		return causeDepartment;
	}

	public void setCauseDepartment(String causeDepartment) {
		this.causeDepartment = causeDepartment;
	}

	public String getScrapQty() {
		return scrapQty;
	}

	public void setScrapQty(String scrapQty) {
		this.scrapQty = scrapQty;
	}

	public String getOutQty() {
		return outQty;
	}

	public void setOutQty(String outQty) {
		this.outQty = outQty;
	}

	public String getTotalScrap() {
		return totalScrap;
	}

	public void setTotalScrap(String totalScrap) {
		this.totalScrap = totalScrap;
	}

	public String getInQty() {
		return inQty;
	}

	public void setInQty(String inQty) {
		this.inQty = inQty;
	}

	public String getCauseRatio() {
		return causeRatio;
	}

	public void setCauseRatio(String causeRatio) {
		this.causeRatio = causeRatio;
	}
	
	
}
